// 인덱스 기반 고정 오프셋을 생성합니다. 각 축은 -2..2 px 범위.
export const getOffsetByIndex = (index: number): { dx: number; dy: number } => {
  // 간단한 선형합동 의사 난수로 고정 값 생성 (seed = index)
  const mod = 5; // 결과를 0..4로 만든 후 -2..2로 매핑
  const dx = ((index * 37 + 11) % mod) - 2;
  const dy = ((index * 53 + 7) % mod) - 2;
  return { dx, dy };
};
